# Calculo volatilidad
def rendimiento(df_final_Cafe):
    df_final_Cafe["Rendimiento"] =  (df_final_Cafe["precio_cafe"].shift(-1) - df_final_Cafe["precio_cafe"])/ df_final_Cafe["precio_cafe"]
    return df_final_Cafe
df_final_Cafe = rendimiento(df_final_Cafe)
df_final_Cafe['Promedio rendimiento'] = df_final_Cafe.groupby('año')['Rendimiento'].transform('mean')

def calcular_diferencia_r(df_final_Cafe):
    df_final_Cafe["Diferencia cuadrado"] = (df_final_Cafe["Rendimiento"]-df_final_Cafe['Promedio rendimiento'])**2
    return df_final_Cafe

# Calculamos la nueva columna
