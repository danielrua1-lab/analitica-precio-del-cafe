def generar_dispersion(df_Cafe, variable):

    config = {

        'costo_produccion_cafe': {
            'titulo': 'Costo de Producción del Café',
            'color': '#2E7D32',
        },

        'indice_dolar': {
            'titulo': 'Índice del Dólar (DXY)',
            'color': '#C58B45',
        },

        'precio_petroleo': {
            'titulo': 'Precio del Petróleo',
            'color': '#8B5A2B',
        },
    }

    c = config[variable]

    fig = px.scatter(

        df_Cafe,

        x=variable,
        y='precio_cafe',

        trendline='ols',

        title=f"<b>Relación entre {c['titulo']} y el Precio del Café</b>",

        color_discrete_sequence=[c['color']]
    )

    fig.update_traces(

        marker=dict(
            size=9,
            opacity=0.75,

            line=dict(
                width=1,
                color='white'
            )
        ),

        hovertemplate=
            "<b>Variable:</b> %{x:.2f}<br>" +
            "<b>Precio Café:</b> %{y:.2f}<extra></extra>"
    )

    if len(fig.data) > 1:

        fig.data[1].update(

            line=dict(
                color='#B22222',
                width=4,
                dash='dot'
            )
        )

    fig.update_layout(

        height=560,

        paper_bgcolor='#F5E9D8',
        plot_bgcolor='#FFF9F2',

        font=dict(
            family='Georgia',
            color='#4B3621'
        ),

        title=dict(
            x=0.5,
            font=dict(size=22)
        ),

        hovermode='closest',

        margin=dict(
            l=60,
            r=40,
            t=80,
            b=70
        ),

        xaxis=dict(
            title=c['titulo'],
            showgrid=False
        ),

        yaxis=dict(
            title='Precio del Café',
            gridcolor='rgba(0,0,0,0.08)',
            zeroline=False
        )
    )

    return fig

